# Практическое задание 6

a = int(input("Введите результаты пробежки первого дня в км\n"))
b = int(input("Введите до скольки вы хотите увеличить дальность пробежки в км\n"))
result_day = 1
while  b - a >= 0:
       a *= 1.1
       result_day += 1

print(f"Вы достигнете требуемых показателей на %.d день" % result_day)